/**
 * bean: Lab_area se encuentra los objetos
 * para la creacion de las areas.
*/
package lab_bean;

public class Lab_area {
	
	int codigo=0;
	String nombre="";
	
	public Lab_area(){
		
	}
	public int getCodigo() {
		return codigo;
	}
	public void setCodigo(int codigo) {
		this.codigo = codigo;
	}
	public String getNombre() {
		return nombre;
	}
	public void setNombre(String nombre) {
		this.nombre = nombre;
	}

}
