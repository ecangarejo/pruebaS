package farc_bean;

public class CrearIvaGrupoUnidad {

	String nombreIva="";
	String valor="";
	String descripcion="";
	
	String nombreGrupo="";
	String observacion="";
	String tipo="";
	
	String nombreUnidad="";
	String sigla="";
	
	String nombreForma="";
	String siglaF="";

	public String getnombreIva() {
		return nombreIva;
	}
	public void setnombreIva(String nombreIva) {
		this.nombreIva = nombreIva;
	}
	
	public String getvalor() {
		return valor;
	}
	public void setvalor(String valor) {
		this.valor = valor;
	}
	
	public String getdescripcion() {
		return descripcion;
	}
	public void setdescripcion(String descripcion) {
		this.descripcion = descripcion;
	}
	
	// bean para los grupos
	public String getnombreGrupo() {
		return nombreGrupo;
	}
	public void setnombreGrupo(String nombreGrupo) {
		this.nombreGrupo = nombreGrupo;
	}
	
	public String getobservacion() {
		return observacion;
	}
	public void setobservacion(String observacion) {
		this.observacion = observacion;
	}
	
	public String gettipo() {
		return tipo;
	}
	public void settipo(String tipo) {
		this.tipo = tipo;
	}
	
	// bean para las unidades de medida
	public String getnombreUnidad() {
		return nombreUnidad;
	}
	public void setnombreUnidad(String nombreUnidad) {
		this.nombreUnidad = nombreUnidad;
	}
	
	public String getsigla() {
		return sigla;
	}
	public void setsigla(String sigla) {
		this.sigla = sigla;
	}
	
	// bean para las Formas Farmaceuticas
	public String getnombreForma() {
		return nombreForma;
	}
	public void setnombreForma(String nombreForma) {
		this.nombreForma = nombreForma;
	}
	
	public String getsiglaF() {
		return siglaF;
	}
	public void setsiglaF(String siglaF) {
		this.siglaF = siglaF;
	}
	
}




	
	

